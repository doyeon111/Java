package exam07;

public class StringTest08 {

	public static void main(String[] args) {
		int data = 65; //65는 대문자 A에 대한 정수값
		System.out.println(data);
		System.out.println((char)data); //A가 나옴
		
		char data2 = 'A';
		System.out.println(data2);
		System.out.println((int)data2); //A의 정수값 65가 출력

	}

}
